public class Nested_loop {

	public static void main(String[] args) {
		for(int no_stud =5 ; no_stud <=10; no_stud++) {
			System.out.println("the value of no_student in a org ="+no_stud);
			for(int no_work = 4; no_work<=10; no_work++) {
				System.out.println(no_work);
			}
		}

	}

}
